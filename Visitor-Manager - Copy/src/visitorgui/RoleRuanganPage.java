    private void delete2ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete2ButtonActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(null, "Anda yakin?", "Konfirmasi", JOptionPane.YES_NO_OPTION)==0){
            int[] selects = this.ruanganList.getSelectedIndices();
            for(int select: selects){                
                new FungsiQuery().deleteData("daftar_ruangan", "nama_ruangan ='" + ruangan.get(select).getData() + "'");
                new FungsiQuery().deleteData("daftar_hakakses", "id_ruangan ='" + ruangan.get(select).getId()+"'");                
            }
            JOptionPane.showMessageDialog(null, "Berhasil hapus!");
            new LogReport().report("RMROOM", nama_admin, ruanganList.getSelectedValue(), null);
            refreshList();
        }
    }//GEN-LAST:event_delete2ButtonActionPerformed

    private void rename1ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rename1ButtonActionPerformed
        // TODO add your handling code here:
        if(roleList.getSelectedValues().length != 1){
            JOptionPane.showMessageDialog(this, "Silahkan pilih satu item untuk rename!");
        }
        else if((roleRename.getText().length() < 3)||(this.roleRename.getText().equals(this.roleList.getSelectedValue()))){
            JOptionPane.showMessageDialog(this, "Silahkan beri nama baru minimal 3 karakter!");
        }
        else{
            int index = this.roleList.getSelectedIndex();
            if(new FungsiQuery().updateData("daftar_role", "nama_role = '" +this.roleRename.getText()+ "'", "id_role='" + role.get(index).getId() +"'")){
                JOptionPane.showMessageDialog(this, "Berhasil rename!");
                new LogReport().report("CHROLE", nama_admin, this.roleRename.getText(), null);
                refreshList();                
            }
            this.roleRename.setText("");
        }
    }//GEN-LAST:event_rename1ButtonActionPerformed

