    public void refreshList(){
        try{
            //seleksi daftar role
            rs = new FungsiQuery().selectData("*", "daftar_role", "1");
            DefaultListModel dlml = new DefaultListModel();
            role.clear();
            while(rs.next()){
                dlml.addElement(rs.getString("nama_role"));
                Vardump r = new Vardump();
                r.setData(rs.getString("id_role"), rs.getString("nama_role"));
                role.add(r);
            }
            roleList.setModel(dlml);
            
            //seleksi daftar ruangan
            rs = new FungsiQuery().selectData("*", "daftar_ruangan", "1");
            DefaultListModel dlmr = new DefaultListModel();
            ruangan.clear();
            while(rs.next()){
                dlmr.addElement(rs.getString("nama_ruangan") + "(" + rs.getString("id_ruangan") + ")");
                Vardump r = new Vardump();
                r.setData(rs.getString("id_ruangan"), rs.getString("nama_ruangan"));
                ruangan.add(r);
            }
            ruanganList.setModel(dlmr);
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
