    private void signButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signButtonActionPerformed
        // TODO add your handling code here
        //melakukan error checking
        if(namaText.getText().length() < 3){
                JOptionPane.showMessageDialog(null, "Nama minimal 3 karakter!", "Gagal", JOptionPane.ERROR_MESSAGE);
                return;
            }
        if(identitasText.getText().length() < 5){
                JOptionPane.showMessageDialog(null, "Nomor identitas minimal 5 karakter!", "Gagal", JOptionPane.ERROR_MESSAGE);
                return;
            }

            //mengecek apakah NUID sudah terdaftar
            try{
                rs = new FungsiQuery().selectData("*", "daftar_tag", "NUID = '" + this.NUIDText.getText() + "'");
                if(rs.next()){
                    int answer = JOptionPane.showConfirmDialog(this, "Ganti data dengan yang baru?", "Data Lama Masih Tersimpan", JOptionPane.YES_NO_OPTION);
                    if(answer == 0){
                        new LogReport().report("CHOWN", nama_admin, this.NUIDText.getText(), "");
                        new FungsiQuery().deleteData("daftar_tag", "NUID = '" + this.NUIDText.getText() + "'");
                    }
                    else
                        return;
                }
            }
            catch(SQLException e){
                System.out.println(e.getMessage());
            }
        
        //mencoba menulis data ke tag
        String roleid = role.get(this.roleCombo.getSelectedIndex()).getId();
        String paket = new Blowfish(roleid + "-" + this.identitasText.getText(), this.NUIDText.getText()).getEncrypt();
        System.out.println("Menulis " + paket + "#");
        serial.sendData("1" + paket + "#");
        if(!serial.readByClick().contains("success")){
            JOptionPane.showMessageDialog(null, "Harap tempelkan kartu ke perangkat saat proses mendaftar!", "Gagal menulis data", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try{
            //mengenkripsi data nomor identitas
            String id = new Blowfish(this.identitasText.getText(), this.NUIDText.getText()).getEncrypt();            
            //      menambahkan data tag ke database         
            if(new FungsiQuery().addData("`daftar_tag`", "`NUID`, `status_aktif`, `status_ruang`, `id_role`, `nama_pemilik`, `no_identitas`",
                    "'"+ this.NUIDText.getText() + "','1', '0','" + role.get(this.roleCombo.getSelectedIndex()).getId()+"', '" + this.namaText.getText()+"','"
                            + id + "'")){
                //mereset field setelah query berhasil
                this.namaText.setText("");
                this.identitasText.setText("");
                this.NUIDText.setText("NUID");
                this.signButton.setEnabled(false);
                JOptionPane.showMessageDialog(null, "Berhasil didaftarkan");
                new LogReport().report("ADDTAG", nama_admin, NUIDText.getText(), null);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_signButtonActionPerformed

