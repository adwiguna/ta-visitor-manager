    private void scanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scanButtonActionPerformed
        // TODO add your handling code here:        
        readData="";
        serial.resetReadPointer();
        if(scanButton.getText().equals("Scan") && port.isOpen()){
            serial.writeData("0");
            readData = serial.readByClick();            
        }
        else{
            scanButton.setText("Scan");
        }
        this.NUIDText.setText(readData);
        if(readData.length() > 7){
            this.signButton.setEnabled(true);
        }
        else{
            this.signButton.setEnabled(false);
        }
    }//GEN-LAST:event_scanButtonActionPerformed

    private void connectButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_connectButtonActionPerformed
        // TODO add your handling code here:        
        if(connectButton.getText().equals("Connect")) {
            //mencoba koneksi ke serial port
            port = SerialPort.getCommPort(comm.get(portCombo.getSelectedIndex()).getData());
            serial = new SerialConn(port);
            if(serial.connectPort()) {
                connectButton.setText("Disconnect");
                portCombo.setEnabled(false);
                scanButton.setEnabled(true);
                scanButton2.setEnabled(true);
            }
        }        
        else {
                // disconnect from the serial port
                port.closePort();
                portCombo.setEnabled(true);
                connectButton.setText("Connect");
                System.out.println("Koneksi diputus");
                scanButton.setEnabled(false);
                scanButton2.setEnabled(false);
        }
    }//GEN-LAST:event_connectButtonActionPerformed
    
