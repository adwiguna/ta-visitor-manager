    public void refreshList(){
        //menampilkan daftar role dalam combo box                
        role.clear();
        roleCombo.removeAllItems();
        
        try{
            rs = new FungsiQuery().selectData("*", "daftar_role", "1");
            while(rs.next()){
                Vardump r = new Vardump();
                r.setData(rs.getString("id_role"), rs.getString("nama_role"));
                role.add(r);
                roleCombo.addItem(r.getData());
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
